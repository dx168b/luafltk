function explode(div,str)
  if (div=='') then return false end
  local pos,arr = 0,{}
  -- for each divider found
  for st,sp in function() return string.find(str,div,pos,true) end do
    table.insert(arr,string.sub(str,pos,st-1)) -- Attach chars left of current divider
    pos = sp + 1 -- Jump past current divider
  end
  table.insert(arr,string.sub(str,pos)) -- Attach chars right of last divider
  return arr
end

function explode2(div,str)
  if (div=='') then return false end
  local pos,arr, find, sub = 0, {}, string.find, string.sub
  -- for each divider found
  while(true) do
	local st,sp = find(str,div,pos,true)
	if not st then break end
    table.insert(arr,sub(str,pos,st-1)) -- Attach chars left of current divider
    pos = sp + 1 -- Jump past current divider
  end
  table.insert(arr,sub(str,pos)) -- Attach chars right of last divider
  return arr
end

